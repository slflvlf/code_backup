%% Explaination of Initialization_data.m
% D           - 线性阻尼矩阵
% M           - 船体质量矩阵
% Ma          - 附加质量矩阵
% P_mean      - 对角线上二次传递函数P对波浪谱的平均值
% QTF_Dir     - 二次传递函数对应的方向向量
% QTF_Freq    - 二次传递函数对应的频率向量
% QTF_P       - 二次传递函数P
% QTF_P1      - 同上
% QTF_Q       - 二次传递函数Q
% QTF_T       - T = sqrt(P^2+Q^2)
% QTF_Phase   - Phase = arctan(-Q/P)
% RAO_Amp     - RAO的幅值
% RAO_Phase   - RAO的相位
% RAO_Dir     - RAO对应的方向
% RAO_Freq    - RAO对应的频率